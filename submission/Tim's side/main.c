void Beep_Buzzer(int times, int delay_ms)
{
  for (int i = 0; i < times; i++) {
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_3, GPIO_PIN_SET);   // BUZZER ON
		HAL_Delay(delay_ms);
		HAL_GPIO_WritePin(GPIOA, GPIO_PIN_3, GPIO_PIN_RESET); // BUZZER OFF
		HAL_Delay(delay_ms);

  }
}
